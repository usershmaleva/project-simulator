export async function loadScenarios() {
  const response = await fetch("./data/scenarios.json");
  if (!response.ok) {
    throw new Error("Could not load scenarios.");
  }
  return response.json();
}

export function getScenarioBySkill(scenariosBySkill, skill) {
  const scenarios = scenariosBySkill[skill];
  return Array.isArray(scenarios) && scenarios.length > 0 ? scenarios[0] : null;
}
